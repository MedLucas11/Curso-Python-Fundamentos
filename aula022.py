# Constantes são informações que não mudam durante a execução do código
# Como python é uma linguagem dinâmica, o recurso de constante não é possível
# Para determinar uma constante num script python escreva o nome da variável em maiúsculo

# Exemplo:

PI = 3.1415
raio = 35
area = PI * (raio*raio)
print(area)
